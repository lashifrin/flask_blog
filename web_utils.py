def calculator(num1, num2):
    """
    Calculates the sum of two numbers.

    :param num1: First number to add
    :type num1: int or float
    :param num2: Second number to add
    :type num2: int or float
    :return: The sum of num1 and num2
    :rtype: int or float
    """
    return num1 + num2